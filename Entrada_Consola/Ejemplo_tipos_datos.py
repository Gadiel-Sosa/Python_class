#Ejemplo de concatenación o suma de datos
numero1 = '10'
print(f'Número 1 en cadena: {numero1}')
numero2 = '20'
print(f'Número 2 en cadena: {numero2}')

resultado = numero1 + numero2
print(f'Resultado en cadena: {resultado}')

numero1_entero = int(numero1)
numero2_entero = int(numero2)

resultado = numero1_entero + numero2_entero
print(f'Resultado enteros: {resultado}')
