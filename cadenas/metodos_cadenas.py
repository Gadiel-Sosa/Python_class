# Metodos de cadenas
cadena1 = 'Hola Mundo'
print(f'Cadena original: {cadena1}')
mayusculas = cadena1.upper() # Convertir a mayusculas
print(f'Cadena en mayusculas: {mayusculas}')

print(f'Cadena minusculas: {cadena1.lower()}') # Convertir a minusculas
cadena2 = ' Juan Perez '
print(f'Cadena con espacios: {cadena2}')
print(f'Cadena con espacios: {cadena2.strip()}') # Eliminar espacios al inicio y al final