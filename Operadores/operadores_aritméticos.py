# Operadores aritméticos

a = 10
b = 3

# Suma +
suma = a + b
print(f'La suma de a y b es: {suma}')

# Resta -
resta = a - b
print('resta: ',resta)

# Multiplicacion
multiplicacion = a * b
print('Multiplicación: ',multiplicacion)
# División / retorna un resultado flotante
division = a / b
print(f'División: {division:.2f}')

# División entera // retorna un valor entero
division_entera = a // b
print(f'División esntera: {division_entera}')

# Módulo % retorna el residuo
modulo = a % b
print(f'Módulo: {modulo}')

# Potencia **

exponente = a ** b
print(f'Expenente: {exponente}')


