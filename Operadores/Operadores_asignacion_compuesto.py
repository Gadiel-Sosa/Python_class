print('*** Operadores de asignacion compuesto ***')
a, b = 10, 15
print(f'Valores iniciales de a = {a} y b = {b}')

#Operador compuesto suma +=
a += b # a = a + b
print(f'Operador a += b: {a}')
#Operador compuesto resta -=
a = 10
a -= b # a = a - b
print(f'Operador a -= b: {a}')

#Operador compuesto divisi√≥n /=
a = 10
a /= b # a = a / b
print(f'Operador a /= b: {a:.2f}')
#Operador compuesto exponente **=
a = 10
a **= b # a = a ** b
print(f'Operador a **= b: {a}')


