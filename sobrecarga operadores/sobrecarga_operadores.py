# Operador suma
'''La sobrecarga de operadores en Python es la capacidad de redefinir cómo se comportan los operadores estándar (+, -, *, ==, <, etc.) cuando se aplican a objetos de una clase definida por el usuario.

Básicamente, te permite hacer que tus clases "se porten" como los tipos nativos de Python, dándoles soporte para operaciones aritméticas, comparaciones y más.
'''
# Suma
a = 2
b = 3
print(a + b)
#Concatenación 
a = 'Hola '
b = 'Mundo'
print(a + b)
# Unión de dos listas
a = [1,2,3]
b = [5,6,7]
print(a + b)


