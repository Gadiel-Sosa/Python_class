# Variables en Python
# declaracion e inicializacion de variables
edad=28
altura=1.65
pais='México'
# Acceder a las variables
print(f'Edad: {edad}')
print(f'Altura: {altura}')
print(f'País: {pais}')
# Modificar el valor de una variable
edad= 30
altura= 1.68
pais='España'
# Acceder a las variables modificadas
print('Valores modificados')
print(f'Edad: {edad}')
print(f'Altura: {altura}')
print(f'País: {pais}')

# En Python el tipo es dinámico
edad='30'
print('Edad:', edad)

# Acceder a una variable no declarada genera error
telefono='55-65-78-34-23'
print('Teléfono:', telefono)